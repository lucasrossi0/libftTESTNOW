#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	br;

	br = '\n';
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &br, 1);
}
